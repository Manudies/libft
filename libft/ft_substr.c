#include "libft.h"

char *ft_substr(const char *s, unsigned int start, size_t len)
{
	size_t	s_len;
	size_t	i;
	char	*substr;

	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	if (start >= s_len)
		return (ft_strdup("")); // devuelves cadena vacÃ­a

	if (len > s_len - start)
		len = s_len - start; // recortas si `len` se pasa

	substr = (char *)malloc(len + 1);
	if (!substr)
		return (NULL);

	i = 0;
	while (i < len)
	{
		substr[i] = s[start + i];
		i++;
	}
	substr[i] = '\0';
	return (substr);
}
